# Type Conversion
# so what is Type Conversion ?
# Lets say we have a string a number or interger and  we need to print that in a str , so in
# in this case we generally use the type conversion

a = str(100)  # here 100 i.e. integer is converted into the string
print(type(a))
b = int(a)  # here what we have done is that we again converted the value in integer
print(type(b))
