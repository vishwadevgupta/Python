# Range

# here it will print numbers from 0 to 99
for i in range(0, 100):
    print(i)

# Here it will print numbers from 0 to 99 with a step of 2
for i in range(0, 100, 2):
    print(i)

# here it will print hi 10 times
for i in range(0, 10):
    print("hi")

# here it will print numbers from 99 to 1
for i in range(99, 0, -1):
    print(i)
