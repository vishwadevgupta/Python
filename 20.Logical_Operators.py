#LOGICAL_OPERATORS

print( 2 > 3) # here it will return false because 2 is not greater than 3
print( 2 < 3) # here it will return true because 2 is less than 3
print( 2 >= 3)# here it will return false because 2 is not greater than or equal to 3
print( 2 <= 3)# here it will return true because 2 is less than or equal to 3
print( 2 == 3)# here it will return false because 2 is not equal to 3
print( 2 != 3)# here it will return true because 2 is not equal to 3
print( 2 > 3 and 2 < 3) # here it will return false because both conditions are not true
print( 2 > 3 or 2 < 3) # here it will return true because one of the conditions is true
print( not 2 > 3) # here it will return true because the condition is false and we are using not operator which will return the opposite of the condition